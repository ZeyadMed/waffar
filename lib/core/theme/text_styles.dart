  // Convenience factory methods to generate commonly needed styles with
  // different sizes/weights. These help cover the "عريضه/رفيعه" (bold/thin),
  // large/small, white text and hint text cases without adding many static
  // fields for every combination.

  /// Returns a bold style with given [fontSize]. Use for large/small bold text.
  static TextStyle boldStyle(
    double fontSize, {
    Color? color,
    FontWeight weight = FontWeight.w700,
  }) => TextStyle(
    fontSize: fontSize.sp,
    color: color ?? AppColors.darkTextColor,
    fontWeight: weight,
    fontFamily: 'IBM Plex Sans Arabic',
  );

  /// Returns a light (thin) style with given [fontSize].
  static TextStyle lightStyle(double fontSize, {Color? color}) => TextStyle(
    fontSize: fontSize.sp,
    color: color ?? AppColors.darkTextColor,
    fontWeight: FontWeight.w300,
    fontFamily: 'IBM Plex Sans Arabic',
  );

  /// Returns a white text style with given [fontSize]. Useful for buttons on
  /// colored backgrounds.
  static TextStyle whiteText(
    double fontSize, {
    FontWeight weight = FontWeight.bold,
  }) => TextStyle(
    fontSize: fontSize.sp,
    color: AppColors.whiteColor,
    fontWeight: weight,
    fontFamily: 'IBM Plex Sans Arabic',
  );

  /// Returns a hint text style with given [fontSize]. Uses the project's
  /// hint color and a light weight.
  static TextStyle hintStyle(double fontSize) => TextStyle(
    fontSize: fontSize.sp,
    color: AppColors.greyColor,
    fontWeight: FontWeight.w300,
    fontFamily: 'IBM Plex Sans Arabic',
  );
